class NotesController < ApplicationController

    def update
        note = Note.find_by!(id: params[:id])
        note.update!(text: params[:text])
        render json: note
    end

    def destroy
        note = Note.find_by!(id: params[:id])
        note.destroy!
        head :no_content
    end

    def create
        note = Note.create!(note_params)
        render json: note
    end

    private
    def note_params
        params.permit(:text, :belongsable_id, :belongsable_type)
    end

end
